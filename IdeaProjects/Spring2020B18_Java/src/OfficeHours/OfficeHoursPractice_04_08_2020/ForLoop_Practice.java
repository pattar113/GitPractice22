package OfficeHours.OfficeHoursPractice_04_08_2020;

public class ForLoop_Practice {



    /*
    for (initialization; Condition; iterator){
    }
    iterator: co-related to your condition

     */
    public static void main(String[] args) {


        for (int i =1;i<=5;i+=2){
            System.out.println("Hello world"+i);
        }

        System.out.println("=======================");


        for(int i=1; i<=5; ){


            i++;        //i= increment by 1 thyen it will be to before the print statement
            System.out.println(i);   // now it will starting to increase "i" value from 2;

        }



    }


}
